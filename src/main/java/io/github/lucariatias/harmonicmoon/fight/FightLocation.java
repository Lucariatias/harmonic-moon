package io.github.lucariatias.harmonicmoon.fight;

public class FightLocation {

    private int x;
    private int y;

    public FightLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
